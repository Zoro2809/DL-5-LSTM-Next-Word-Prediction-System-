raw_text = re.sub(r'[^a-zA-Z\s]', '', raw_text)   # keep only letters and spaces
raw_text = re.sub(r'\s+', ' ', raw_text)           # collapse multiple spaces
raw_text = raw_text.lower().strip()               # lowercase everything

